package com.yash.springioc.que6;

public class Engine {
 
	private String engine_type;
	private String engine_power;
	private String engine_name;
	public String getEngine_type() {
		return engine_type;
	}
	public void setEngine_type(String engine_type) {
		this.engine_type = engine_type;
	}
	public String getEngine_power() {
		return engine_power;
	}
	public void setEngine_power(String engine_power) {
		this.engine_power = engine_power;
	}
	public String getEngine_name() {
		return engine_name;
	}
	public void setEngine_name(String engine_name) {
		this.engine_name = engine_name;
	}
	
	
}
